// 获取风格
export function getWriterStyle(id: string): WriterStyle | undefined {
  return WRITER_STYLES.find(style => style.id === id);
}

// 获取所有风格
export function getAllWriterStyles(): WriterStyle[] {
  return WRITER_STYLES;
}

// 按流派分组
export function getStylesByGenre(): Record<string, WriterStyle[]> {
  const grouped: Record<string, WriterStyle[]> = {};
  WRITER_STYLES.forEach(style => {
    const genre = style.genre.split('·')[0].trim();
    if (!grouped[genre]) {
      grouped[genre] = [];
    }
    grouped[genre].push(style);
  });
  return grouped;
}

// 随机获取一个风格
export function getRandomWriterStyle(): WriterStyle {
  return WRITER_STYLES[Math.floor(Math.random() * WRITER_STYLES.length)];
}

